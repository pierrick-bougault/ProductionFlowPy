def set_language(lang: str):
    """D√©finit la langue courante / Sets current language"""
    global _current_language
    if lang in TRANSLATIONS:
        _current_language = lang


def get_language() -> str:
    """Retourne la langue courante / Returns current language"""
    return _current_language


def tr(key: str, default: str = None) -> str:
    """
    Traduit une cl√© dans la langue courante.
    
    Args:
        key: Cl√© de traduction
        default: Valeur par d√©faut si la cl√© n'existe pas
        
    Returns:
        Texte traduit ou valeur par d√©faut
    """
    translations = TRANSLATIONS.get(_current_language, TRANSLATIONS['fr'])
    if key in translations:
        return translations[key]
    # Fallback vers le fran√ßais si cl√© manquante en anglais
    if _current_language != 'fr' and key in TRANSLATIONS['fr']:
        return TRANSLATIONS['fr'][key]
    return default if default is not None else key


